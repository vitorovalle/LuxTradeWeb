    private void imagemSelecionadaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imagemSelecionadaBtnActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(this);
        File selectedImage = fc.getSelectedFile();
        path = selectedImage.getName();
        
        try {
            Image img = ImageIO.read(selectedImage);
            localImagemCampo.setText(path);
            imagemSelecionadaCampo.setIcon(new ImageIcon(img.getScaledInstance(imagemSelecionadaCampo.getWidth(),
                    imagemSelecionadaCampo.getHeight(), Image.SCALE_SMOOTH)));
        } catch (IOException ex) {
            Logger.getLogger(AdminPanel.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_imagemSelecionadaBtnActionPerformed

