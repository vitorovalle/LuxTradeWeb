    private void atualizaEstoqueBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizaEstoqueBtnActionPerformed
        // TODO add your handling code here:
       try{
            String model = modeloAddCampo.getText();
            int qty = Integer.parseInt(atualizaQtdeCampo.getText());

            if(categoria2ComboBox.getSelectedItem().equals("Iluminacao")){
                IluminacaoDAO.flag = true;
                IluminacaoDAO.atualizarIluminacao(model, qty);
            }

            else if(categoria2ComboBox.getSelectedItem().equals("Hidraulica")){
                HidraulicaDAO.flag = true;
                HidraulicaDAO.atualizarHidraulica(model, qty);
            }

            else if(categoria2ComboBox.getSelectedItem().equals("Eletro")){
                EletroDAO.flag = true;
                EletroDAO.atualizarEletro(model, qty);
            }
            
            modeloAddCampo.setText("");
            atualizaQtdeCampo.setText("");
        
       }catch(Exception e){
           JOptionPane.showMessageDialog(null,e);
       }
        
   
    }//GEN-LAST:event_atualizaEstoqueBtnActionPerformed

